// How to use .push(), .pop(), .shift() and .unshift()
var pokemonArray = ['pickachu', 'squirtle', 'charmander']

// add contents of quotes to the end of the Pokemon array
pokemonArray.push('bulbasaur')
// console the Pokemon array after push
console.log(pokemonArray)

// remove the last item in the Pokemon array
pokemonArray.pop()
// console the Pokemon array after pop
console.log(pokemonArray)

// add contents of quotes to the start of the Pokemon array
pokemonArray.unshift('meowth')
// console the Pokemon array after unshift
console.log(pokemonArray)

// remove the first item in the Pokemon array
pokemonArray.shift()
// console the Pokemon array after shift
console.log(pokemonArray)
